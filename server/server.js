require("dotenv").config();
const express = require("express");
const mongoose = require("mongoose");
const cors = require("cors");

const app = express();
app.use(express.json());
app.use(cors({ origin: "*" })); // Allow all origins (Update as needed)

// âœ… MongoDB Connection
mongoose
  .connect(process.env.MONGODB_URI, {
    useNewUrlParser: true,
    useUnifiedTopology: true,
  })
  .then(() => console.log("âœ… Connected to MongoDB"))
  .catch((err) => console.error("âŒ MongoDB connection error:", err));

// âœ… Root Route (Fixes "Cannot GET /" issue)
app.get("/", (req, res) => {
  res.send("âœ… Backend is running on Vercel! ðŸš€");
});

// âœ… User Schema & Model
const userSchema = new mongoose.Schema({
  name: { type: String, required: true },
  email: { type: String, required: true, unique: true },
});

const User = mongoose.model("User", userSchema);

// âœ… API Routes
app.get("/api/users", async (req, res) => {
  try {
    const users = await User.find();
    res.json(users);
  } catch (error) {
    res.status(500).json({ message: "Server error" });
  }
});

app.post("/api/users", async (req, res) => {
  try {
    const { name, email } = req.body;
    const newUser = new User({ name, email });
    await newUser.save();
    res.status(201).json(newUser);
  } catch (error) {
    res.status(400).json({ message: "Invalid data" });
  }
});

app.put("/api/users/:id", async (req, res) => {
  try {
    const updatedUser = await User.findByIdAndUpdate(req.params.id, req.body, {
      new: true,
    });
    res.json(updatedUser);
  } catch (error) {
    res.status(404).json({ message: "User not found" });
  }
});

app.delete("/api/users/:id", async (req, res) => {
  try {
    await User.findByIdAndDelete(req.params.id);
    res.json({ message: "User deleted" });
  } catch (error) {
    res.status(404).json({ message: "User not found" });
  }
});

// âœ… Use the correct PORT for Vercel
const PORT = process.env.PORT || 5000;

// âœ… Export app for Vercel
module.exports = app;

// âœ… Start server only if NOT in Vercel environment
if (require.main === module) {
  app.listen(PORT, () => console.log(`ðŸš€ Server running on port ${PORT}`));
}
